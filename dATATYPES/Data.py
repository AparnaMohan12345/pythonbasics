"""
Data Types

1.string(set of characters) - Immutable datatype
2.List - Mutable
3.Tuple - Immutable
4.Dictionary - Mutable
5.Set - Immutable
6.Number - Immutable
"""

#string
name = 'Aparna'
print(type(name))

#int
a = 2
print(type(a))

#list
names = ['appu', 'dhoppu', 'tikku']
print(names)
print(type(names))

#dictionary
names = {'name1': 'appu', 'name2': 'dhoppu', 'name3': 'tikku'}
print(names)
print(type(names))

#tuple
names = ('appu', 'dhoppu', 'tikku')
print(names)
print(type(names))

#set
names = {'appu', 'dhoppu', 'tikku'}
print(names)
print(type(names))